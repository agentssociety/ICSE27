from __future__ import annotations

from typing import Optional, Protocol

from src.dto.attempt_review.attempt_review_dto import AttemptReviewCreate, AttemptReviewUpdate, AttemptReviewResponse


class AttemptReviewRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[AttemptReviewResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[AttemptReviewResponse]: ...
    def create(self, data: AttemptReviewCreate) -> AttemptReviewResponse: ...
    def update(self, item_id: int, data: AttemptReviewUpdate) -> Optional[AttemptReviewResponse]: ...
    def delete(self, item_id: int) -> bool: ...
