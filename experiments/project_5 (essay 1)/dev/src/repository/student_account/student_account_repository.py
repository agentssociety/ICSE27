from __future__ import annotations

from typing import Optional, Protocol

from src.dto.student_account.student_account_dto import StudentAccountCreate, StudentAccountUpdate, StudentAccountResponse


class StudentAccountRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[StudentAccountResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[StudentAccountResponse]: ...
    def create(self, data: StudentAccountCreate) -> StudentAccountResponse: ...
    def update(self, item_id: int, data: StudentAccountUpdate) -> Optional[StudentAccountResponse]: ...
    def delete(self, item_id: int) -> bool: ...
