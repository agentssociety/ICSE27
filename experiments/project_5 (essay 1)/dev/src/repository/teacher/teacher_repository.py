from __future__ import annotations

from typing import Optional, Protocol

from src.dto.teacher.teacher_dto import TeacherCreate, TeacherUpdate, TeacherResponse


class TeacherRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[TeacherResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[TeacherResponse]: ...
    def create(self, data: TeacherCreate) -> TeacherResponse: ...
    def update(self, item_id: int, data: TeacherUpdate) -> Optional[TeacherResponse]: ...
    def delete(self, item_id: int) -> bool: ...
