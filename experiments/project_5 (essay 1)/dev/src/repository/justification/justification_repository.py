from __future__ import annotations

from typing import Optional, Protocol

from src.dto.justification.justification_dto import JustificationCreate, JustificationUpdate, JustificationResponse


class JustificationRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[JustificationResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[JustificationResponse]: ...
    def create(self, data: JustificationCreate) -> JustificationResponse: ...
    def update(self, item_id: int, data: JustificationUpdate) -> Optional[JustificationResponse]: ...
    def delete(self, item_id: int) -> bool: ...
