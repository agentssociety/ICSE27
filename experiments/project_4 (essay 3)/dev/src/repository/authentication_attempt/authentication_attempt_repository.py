from __future__ import annotations

from typing import Optional, Protocol

from src.dto.authentication_attempt.authentication_attempt_dto import AuthenticationAttemptCreate, AuthenticationAttemptUpdate, AuthenticationAttemptResponse


class AuthenticationAttemptRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[AuthenticationAttemptResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[AuthenticationAttemptResponse]: ...
    def create(self, data: AuthenticationAttemptCreate) -> AuthenticationAttemptResponse: ...
    def update(self, item_id: int, data: AuthenticationAttemptUpdate) -> Optional[AuthenticationAttemptResponse]: ...
    def delete(self, item_id: int) -> bool: ...
