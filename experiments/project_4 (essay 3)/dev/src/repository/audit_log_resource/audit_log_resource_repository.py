from __future__ import annotations

from typing import Optional, Protocol

from src.dto.audit_log_resource.audit_log_resource_dto import AuditLogResourceCreate, AuditLogResourceUpdate, AuditLogResourceResponse


class AuditLogResourceRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[AuditLogResourceResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[AuditLogResourceResponse]: ...
    def create(self, data: AuditLogResourceCreate) -> AuditLogResourceResponse: ...
    def update(self, item_id: int, data: AuditLogResourceUpdate) -> Optional[AuditLogResourceResponse]: ...
    def delete(self, item_id: int) -> bool: ...
