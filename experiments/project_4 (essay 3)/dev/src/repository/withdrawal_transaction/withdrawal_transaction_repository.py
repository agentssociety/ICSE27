from __future__ import annotations

from typing import Optional, Protocol

from src.dto.withdrawal_transaction.withdrawal_transaction_dto import WithdrawalTransactionCreate, WithdrawalTransactionUpdate, WithdrawalTransactionResponse


class WithdrawalTransactionRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[WithdrawalTransactionResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[WithdrawalTransactionResponse]: ...
    def create(self, data: WithdrawalTransactionCreate) -> WithdrawalTransactionResponse: ...
    def update(self, item_id: int, data: WithdrawalTransactionUpdate) -> Optional[WithdrawalTransactionResponse]: ...
    def delete(self, item_id: int) -> bool: ...
