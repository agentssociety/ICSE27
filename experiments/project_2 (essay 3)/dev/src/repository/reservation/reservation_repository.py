from __future__ import annotations

from typing import Optional, Protocol

from src.dto.reservation.reservation_dto import ReservationCreate, ReservationUpdate, ReservationResponse


class ReservationRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[ReservationResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[ReservationResponse]: ...
    def create(self, data: ReservationCreate) -> ReservationResponse: ...
    def update(self, item_id: int, data: ReservationUpdate) -> Optional[ReservationResponse]: ...
    def delete(self, item_id: int) -> bool: ...
