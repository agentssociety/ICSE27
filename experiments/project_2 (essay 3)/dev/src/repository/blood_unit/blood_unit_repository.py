from __future__ import annotations

from typing import Optional, Protocol

from src.dto.blood_unit.blood_unit_dto import BloodUnitCreate, BloodUnitUpdate, BloodUnitResponse


class BloodUnitRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[BloodUnitResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[BloodUnitResponse]: ...
    def create(self, data: BloodUnitCreate) -> BloodUnitResponse: ...
    def update(self, item_id: int, data: BloodUnitUpdate) -> Optional[BloodUnitResponse]: ...
    def delete(self, item_id: int) -> bool: ...
