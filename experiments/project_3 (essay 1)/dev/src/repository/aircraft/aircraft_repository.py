from __future__ import annotations

from typing import Optional, Protocol

from src.dto.aircraft.aircraft_dto import AircraftCreate, AircraftUpdate, AircraftResponse


class AircraftRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[AircraftResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[AircraftResponse]: ...
    def create(self, data: AircraftCreate) -> AircraftResponse: ...
    def update(self, item_id: int, data: AircraftUpdate) -> Optional[AircraftResponse]: ...
    def delete(self, item_id: int) -> bool: ...
