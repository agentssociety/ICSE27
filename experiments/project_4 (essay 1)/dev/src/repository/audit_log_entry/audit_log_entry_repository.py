from __future__ import annotations

from typing import Optional, Protocol

from src.dto.audit_log_entry.audit_log_entry_dto import AuditLogEntryCreate, AuditLogEntryUpdate, AuditLogEntryResponse


class AuditLogEntryRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[AuditLogEntryResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[AuditLogEntryResponse]: ...
    def create(self, data: AuditLogEntryCreate) -> AuditLogEntryResponse: ...
    def update(self, item_id: int, data: AuditLogEntryUpdate) -> Optional[AuditLogEntryResponse]: ...
    def delete(self, item_id: int) -> bool: ...
