from __future__ import annotations

from typing import Optional, Protocol

from src.dto.biography.biography_dto import BiographyCreate, BiographyUpdate, BiographyResponse


class BiographyRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[BiographyResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[BiographyResponse]: ...
    def create(self, data: BiographyCreate) -> BiographyResponse: ...
    def update(self, item_id: int, data: BiographyUpdate) -> Optional[BiographyResponse]: ...
    def delete(self, item_id: int) -> bool: ...
