from __future__ import annotations

from typing import Optional, Protocol

from src.dto.block_record.block_record_dto import BlockRecordCreate, BlockRecordUpdate, BlockRecordResponse


class BlockRecordRepository(Protocol):
    def get_by_id(self, item_id: int) -> Optional[BlockRecordResponse]: ...
    def get_all(self, skip: int = 0, limit: int = 100) -> list[BlockRecordResponse]: ...
    def create(self, data: BlockRecordCreate) -> BlockRecordResponse: ...
    def update(self, item_id: int, data: BlockRecordUpdate) -> Optional[BlockRecordResponse]: ...
    def delete(self, item_id: int) -> bool: ...
