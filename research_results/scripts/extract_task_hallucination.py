"""
Task Hallucination Extraction — Deleted Task Detection
=======================================================

Detects tasks that were generated by the agent (logged in run.log as
"Successfully created the task") but are no longer present in agent.md,
meaning they were deleted by the human reviewer after the run.

This is the TASK-LEVEL structural hallucination metric — distinct from:
  - Code-level structural issues (orphan routes, unused imports)
  - NLI spec-extrapolation rate (ungrounded claims)

Definition:
  A "deleted task" = a task whose creation is confirmed in run.log
  (via "Successfully created the task" log lines) but whose ID does NOT
  appear in either:
    (a) task_implementation_order.json  (final task execution list)
    (b) agent.md                        (persistent project record)

  These deletions represent the human operator's judgment that the agent
  generated a task that should not exist — either a duplicate (most common)
  or a task that contradicts or exceeds the project specification.

Root causes observed in the data:
  1. Duplicate creation (most common): pipeline restarted mid-run and the
     agent re-generated identical tasks (e.g., P1E3: tasks 59-64 are exact
     duplicates of 53-58 from the first run).
  2. Over-generation: agent created more tasks than instructed, and the
     human deleted the excess (e.g., P4E3: tasks 102-104 are near-duplicates
     of existing tasks with slightly different scoping).
  3. Single retry-replaced: a task was created, then re-created with a
     corrected title/scope (P4E1: task 81 replaced by task 82 with same name).

Metric definitions:
  n_deleted_tasks         = |created| - |kept| = count of deleted tasks per run
  task_deletion_rate      = n_deleted / n_created  (0 = perfect, 1 = all deleted)
  duplicate_fraction      = n_duplicate_deleted / n_deleted  (how many are exact/near dupes)

Aggregation: mean ± std (ddof=1) across 3 seeds per project.

Output:
  data/task_hallucination/P{N}E{K}_task_halluc.json  — raw per-run data
  summaries/rq2_task_hallucination.json              — aggregated mean ± std

Usage (from repo root):
  python -m research_results.scripts.extract_task_hallucination
"""
from __future__ import annotations

import json
import math
import re
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent.parent))
from research_results.scripts.paths import ALL_RUNS, RUNS_BY_PROJECT, RESULTS_ROOT

DATA_DIR    = RESULTS_ROOT / "data" / "task_hallucination"
SUMMARY_DIR = RESULTS_ROOT / "summaries"


# ── task ID extraction from log ───────────────────────────────────────────────

def _extract_created_task_ids(log_path: Path) -> dict[int, str]:
    """
    Parse run.log and return {task_id: title} for every task confirmed created.

    Patterns detected:
      1. "Successfully created the task {\\n  \"id\": N,\\n  \"title\": \"...\""
         (task_tools.py INFO line followed by JSON)
      2. "Task N created with ID: X"   (agent print statement)
      3. "Created task IDs: [X, Y, Z]" (batch creation summary)
    """
    text  = log_path.read_text(errors="ignore")
    lines = text.splitlines()
    created: dict[int, str] = {}

    for i, line in enumerate(lines):
        if "Successfully created the task" in line:
            context = "\n".join(lines[i : i + 10])
            id_m    = re.search(r'"task_id"\s*:\s*(\d+)|"id"\s*:\s*(\d+)', context)
            title_m = re.search(r'"title"\s*:\s*"([^"]+)"', context)
            if id_m:
                task_id = int(next(g for g in id_m.groups() if g))
                title   = title_m.group(1) if title_m else "<no title>"
                created[task_id] = title

        # "Task N created with ID: X"
        m = re.search(r"Task \d+ created with ID:\s*(\d+)", line)
        if m:
            tid = int(m.group(1))
            if tid not in created:
                created[tid] = "<no title>"

        # "Created task IDs: [X, Y, Z]"
        m = re.search(r"Created task IDs:\s*\[([0-9, ]+)\]", line)
        if m:
            for num in m.group(1).split(","):
                num = num.strip()
                if num:
                    tid = int(num)
                    if tid not in created:
                        created[tid] = "<no title>"

    return created


def _get_surviving_task_ids(run_id) -> set[int]:
    """
    Task IDs that survived human review:
      - present in task_implementation_order.json  AND
      - present in agent.md (as '### Task #N')

    A task is "deleted" if it was logged as created but absent from both.
    We use agent.md as the authoritative record since the human edits it.
    """
    impl_path  = run_id.path / "task_implementation_order.json"
    agent_path = run_id.path / "agent.md"

    impl_ids: set[int] = set()
    if impl_path.exists():
        impl_ids = set(json.loads(impl_path.read_text(encoding="utf-8")))

    agent_ids: set[int] = set()
    if agent_path.exists():
        text = agent_path.read_text(encoding="utf-8")
        agent_ids = {int(m) for m in re.findall(r"^### Task #(\d+)", text, re.MULTILINE)}

    # Union: keep anything surviving in either source
    return impl_ids | agent_ids


def _is_duplicate(title: str, surviving_titles: list[str], threshold: float = 0.7) -> bool:
    """
    Simple word-overlap check: if >70% of the deleted task's words appear
    in any surviving task title, classify it as a duplicate.
    """
    title_words = set(title.lower().split())
    if not title_words:
        return False
    for s in surviving_titles:
        s_words = set(s.lower().split())
        if not s_words:
            continue
        overlap = len(title_words & s_words) / min(len(title_words), len(s_words))
        if overlap >= threshold:
            return True
    return False


# ── per-run extraction ────────────────────────────────────────────────────────

def extract_run(run_id) -> dict:
    log_path = run_id.log_path

    if not log_path.exists():
        return {
            "run_id": str(run_id), "project": run_id.project, "essay": run_id.essay,
            "n_created": 0, "n_kept": 0, "n_deleted": 0,
            "task_deletion_rate": 0.0, "deleted_tasks": [], "kept_tasks": [],
        }

    created_map  = _extract_created_task_ids(log_path)         # {id: title}
    surviving    = _get_surviving_task_ids(run_id)             # set of kept IDs

    # Only consider tasks that were confirmed logged as created
    # (some runs have no creation logs — different log format or truncated)
    if not created_map:
        # Fall back: surviving == all tasks; none deleted from log perspective
        return {
            "run_id": str(run_id), "project": run_id.project, "essay": run_id.essay,
            "n_created": len(surviving), "n_kept": len(surviving), "n_deleted": 0,
            "task_deletion_rate": 0.0,
            "no_creation_log": True,
            "deleted_tasks": [], "kept_tasks": sorted(surviving),
        }

    kept_ids    = {tid for tid in created_map if tid in surviving}
    deleted_ids = {tid for tid in created_map if tid not in surviving}

    surviving_titles = [created_map[tid] for tid in kept_ids]
    deleted_detail   = [
        {
            "task_id":     tid,
            "title":       created_map[tid],
            "is_duplicate": _is_duplicate(created_map[tid], surviving_titles),
        }
        for tid in sorted(deleted_ids)
    ]

    n_created = len(created_map)
    n_deleted = len(deleted_ids)
    n_kept    = len(kept_ids)
    rate      = round(n_deleted / n_created, 4) if n_created else 0.0

    return {
        "run_id":              str(run_id),
        "project":             run_id.project,
        "essay":               run_id.essay,
        "n_created":           n_created,
        "n_kept":              n_kept,
        "n_deleted":           n_deleted,
        "task_deletion_rate":  rate,
        "n_duplicate_deleted": sum(1 for d in deleted_detail if d["is_duplicate"]),
        "deleted_tasks":       deleted_detail,
        "kept_tasks":          sorted(kept_ids),
    }


# ── stats helpers ─────────────────────────────────────────────────────────────

def _mean(v: list[float]) -> float: return sum(v) / len(v) if v else 0.0
def _std(v: list[float]) -> float:
    if len(v) < 2: return 0.0
    m = _mean(v)
    return math.sqrt(sum((x - m) ** 2 for x in v) / (len(v) - 1))

def _stat(vals: list[float], decimals: int = 4) -> dict:
    return {
        "mean":   round(_mean(vals),   decimals),
        "std":    round(_std(vals),    decimals),
        "values": [round(v, decimals) for v in vals],
    }


# ── per-project aggregation ───────────────────────────────────────────────────

def aggregate_project(project_id: int, runs: list[dict]) -> dict:
    n_del   = [float(r["n_deleted"])          for r in runs]
    rates   = [r["task_deletion_rate"]         for r in runs]
    n_dupes = [float(r.get("n_duplicate_deleted", 0)) for r in runs]

    return {
        "project_id":          project_id,
        "n_essays":            len(runs),
        "n_deleted_tasks":     _stat(n_del, 2),
        "task_deletion_rate":  _stat(rates, 4),
        "n_duplicate_deleted": _stat(n_dupes, 2),
        "per_essay": [
            {
                "essay":             r["essay"],
                "n_created":         r["n_created"],
                "n_kept":            r["n_kept"],
                "n_deleted":         r["n_deleted"],
                "task_deletion_rate": r["task_deletion_rate"],
                "deleted_tasks":     r.get("deleted_tasks", []),
            }
            for r in runs
        ],
    }


# ── main ─────────────────────────────────────────────────────────────────────

def run() -> None:
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    SUMMARY_DIR.mkdir(parents=True, exist_ok=True)

    print("=== Task Hallucination (Deleted Task) Extraction ===\n")

    all_run_data: dict[str, dict] = {}
    for run_id in ALL_RUNS:
        print(f"  Scanning {run_id.folder_name} ...", end=" ", flush=True)
        data = extract_run(run_id)
        all_run_data[str(run_id)] = data

        out = DATA_DIR / f"{run_id}_task_halluc.json"
        out.write_text(json.dumps(data, indent=2, ensure_ascii=False), encoding="utf-8")

        deleted_info = ""
        if data["n_deleted"] > 0:
            titles = [d["title"][:40] for d in data.get("deleted_tasks", [])]
            deleted_info = f"  → DELETED: {titles}"
        print(
            f"done  (created={data['n_created']}, kept={data['n_kept']}, "
            f"deleted={data['n_deleted']}, rate={data['task_deletion_rate']:.1%})"
            + deleted_info
        )

    projects_summary = []
    for project_id, run_ids in sorted(RUNS_BY_PROJECT.items()):
        runs = [all_run_data[str(r)] for r in run_ids]
        agg  = aggregate_project(project_id, runs)
        projects_summary.append(agg)

    # Cross-project totals
    total_created = sum(r["n_created"] for r in all_run_data.values())
    total_deleted = sum(r["n_deleted"] for r in all_run_data.values())

    output = {
        "metric": "rq2_task_hallucination",
        "description": (
            "Task-level structural hallucination: tasks created by the agent "
            "(confirmed via 'Successfully created the task' in run.log) that were "
            "subsequently deleted by the human operator (absent from agent.md and "
            "task_implementation_order.json). "
            "Root causes: (1) duplicate creation from pipeline restarts — the agent "
            "re-generated already-existing tasks; (2) over-generation — agent created "
            "more tasks than specified; (3) retry-replacement — a task was created then "
            "re-created with corrections. "
            "task_deletion_rate = n_deleted / n_created per run. "
            "Runs with no 'Successfully created' log entries (different log format) "
            "are treated as 0 deletions (conservative). "
            "Values reported as mean ± std (ddof=1) across 3 seeds per project."
        ),
        "totals": {
            "total_tasks_created_in_logs": total_created,
            "total_tasks_deleted":         total_deleted,
            "overall_deletion_rate":       round(total_deleted / total_created, 4) if total_created else 0.0,
            "runs_with_deletions":         sum(1 for r in all_run_data.values() if r["n_deleted"] > 0),
        },
        "projects": projects_summary,
    }

    out_path = SUMMARY_DIR / "rq2_task_hallucination.json"
    out_path.write_text(json.dumps(output, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"\n  Summary written: {out_path.name}")
    print(f"  Overall: {total_deleted}/{total_created} tasks deleted "
          f"({100*total_deleted/total_created:.1f}%) across {len(all_run_data)} runs")


if __name__ == "__main__":
    run()
